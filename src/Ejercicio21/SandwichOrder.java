package Ejercicio21;

public class SandwichOrder {
    private int size;
    private boolean jalapenos;
    private boolean bacon;
    private boolean turkey;
    private boolean cheese;
    public int getSize() {
        return size;
    }
    public void setSize(int size) {
        this.size = size;
    }
    public boolean hasJalapenos() {
        return jalapenos;
    }
    public void setJalapenos(boolean jalapenos) {
        this.jalapenos = jalapenos;
    }
    public boolean hasBacon() {
        return bacon;
    }
    public void setBacon(boolean bacon) {
        this.bacon = bacon;
    }
    public boolean hasTurkey() {
        return turkey;
    }
    public void setTurkey(boolean turkey) {
        this.turkey = turkey;
    }
    public boolean hasCheese() {
        return cheese;
    }
    public void setCheese(boolean cheese) {
        this.cheese = cheese;
    }
}
