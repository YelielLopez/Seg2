package EstructurasDeControl;
/*🎯 Tarea 3: Bucle for
Escribe un programa que imprima los números del 1 al 10 utilizando un bucle for.*/
public class EstructuraFor {
    public static void main(String[] args) {


        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
