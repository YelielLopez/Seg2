package EstructurasDeControl;
/*🎯 Tarea 4: Bucle while
Crea un programa que imprima los números del 1 al 5 utilizando un bucle while.*/
public class EstructuraWhile {
    public static void main(String[] args) {
        int i = 1;

        while (i<=5) {
            System.out.println(i);
            i++;
        }

    }
}

